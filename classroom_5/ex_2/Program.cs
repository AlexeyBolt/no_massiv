void PrintImage(int[,]pic) //добавили метод для вывода печати
{
for (int rows = 0; rows < pic.GetLength(0); rows++)  // можно написать for (int rows = 0; rows < 3(строки); rows++)
{
    for (int colu = 0; colu < pic.GetLength(1); colu++) // можно написать for (int colu = 0; rows < 3(столбца); rows++)
    {
    /* Console.Write($"{pic[rows,colu]} "); */
    if (pic[rows,colu]==0) Console.Write($" ");
    else
    {
        Console.Write($"*");
    }
    }
    Console.WriteLine(); // чтоб выводилось корректно, следует добавить эту строчку
}
}
void FillImage(int rows, int colu) ///метод закрашивания
{
